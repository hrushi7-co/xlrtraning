public class PrintArrayIndices {
    public static void main(String[] args) {
        // Define an array
        int[] arr = {10, 20, 30, 40, 50};

        // Loop through the array and print the index values
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Index " + i + ": " + arr[i]);
        }
    }
}
    
